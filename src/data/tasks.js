// Group tasks into "lessons" of 5 tasks each (like Duolingo's structure)
export function getTasksForLesson(levelKey, lessonIndex) {
  const tasks = TASKS[levelKey];
  const start = lessonIndex * 5;
  return tasks.slice(start, start + 5);
}

export function getLessonCount(levelKey) {
  return Math.ceil(TASKS[levelKey].length / 5);
}
